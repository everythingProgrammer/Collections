package BitManipulations;

public class BitOperations {

    public static void Or(int a, int b){
        System.out.println(a|b);

    }
    public static void And( int a , int b){
        System.out.println(a&b);
    }

    public static void main(String[] args) {
        Or(29,25);
        And(29,25);
    }

}
